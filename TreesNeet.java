class TreesNeet {

    public static class TreeNodeLeet {
        int val;
        TreeNodeLeet left;
        TreeNodeLeet right;

        TreeNodeLeet() {
        }

        TreeNodeLeet(int val) {
            this.val = val;
        }

        TreeNodeLeet(int val, TreeNodeLeet left, TreeNodeLeet right) {
            this.val = val;
            this.left = left;
            this.right = right;
        }

    }

}
